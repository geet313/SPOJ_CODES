import java.util.*;
import java.lang.*;

class Main
{
	public static void main (String[] args) throws java.lang.Exception
	{
			try{
			Scanner scan=new Scanner(System.in);
			long t = scan.nextLong();
			while(t>0)
			{
				long n = scan.nextLong();
				if(n%3==0)
				{
					System.out.println("Ragini");
				}
				else
				{
					System.out.println("Hemlata");
				}
				
			t--;}

	}catch(Exception e)
	{
		return;
	}
	}
}